def filter_content(content):
    """
    过滤内容中的屏蔽词，将屏蔽词替换为"*"
    :param content: 原始内容
    :return: 过滤后的内容
    """
    if not content:
        return content
    
    filtered_content = content
    for word in FILTER_WORDS:
        if word in filtered_content:
            # 将屏蔽词替换为等长的"*"
            filtered_content = filtered_content.replace(word, '*' * len(word))
    
    return filtered_content


